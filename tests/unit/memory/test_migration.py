def test_prefixed_text_stream_prefixes_each_line():
    """_PrefixedTextStream prepends the prefix to the start of each line, across separate writes."""
    import io

    buffer = io.StringIO()
    stream = _PrefixedTextStream(stream=buffer, prefix="[tag] ")

    # Alembic writes the message and the trailing newline as separate calls.
    assert stream.write("first line") == len("first line")
    assert stream.write("\n") == 1
    stream.write("second\nthird\n")

    assert buffer.getvalue() == "[tag] first line\n[tag] second\n[tag] third\n"


def test_prefixed_text_stream_delegates_attributes():
    """_PrefixedTextStream delegates unknown attributes (e.g. encoding) to the wrapped stream."""
    import io

    buffer = io.StringIO()
    stream = _PrefixedTextStream(stream=buffer, prefix="[tag] ")

    assert stream.getvalue() == ""
    stream.flush()  # delegated, should not raise


def test_alembic_env_raises_when_no_connection():
    """Covers env.py line 15: RuntimeError when connection is None."""
    import importlib
    import sys
    from unittest.mock import MagicMock

    # Build a mock alembic context where config.attributes has no "connection"
    mock_config = MagicMock()
    mock_config.attributes = {}  # .get("connection") → None

    mock_context = MagicMock()
    mock_context.config = mock_config

    # Remove cached env module so reload runs module-level code
    env_module_name = "pyrit.memory.alembic.env"
    saved = sys.modules.pop(env_module_name, None)

    # Patch alembic.context to be our mock context
    import alembic

    original_context = getattr(alembic, "context", None)
    alembic.context = mock_context

    try:
        with pytest.raises(RuntimeError, match="No connection found for Alembic migration"):
            importlib.import_module(env_module_name)
    finally:
        # Restore original state
        alembic.context = original_context
        sys.modules.pop(env_module_name, None)
        if saved is not None:
            sys.modules[env_module_name] = saved


def _get_alembic_head_revision(*, config: Config) -> str:
    """Return the current Alembic head revision for the configured script location."""
    head_revision = ScriptDirectory.from_config(config).get_current_head()
    if head_revision is None:
        raise RuntimeError("No Alembic head revision found for memory migrations.")

    return head_revision


